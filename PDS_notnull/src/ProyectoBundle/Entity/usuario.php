        $this->rol = $rol;
    
        return $this;
    }

    /**
     * Get rol
     *
     * @return string
     */
    public function getRol()
    {
        return $this->rol;
    }

    /**
     * Set password
     *
     * @param string $password
     *
     * @return usuario
